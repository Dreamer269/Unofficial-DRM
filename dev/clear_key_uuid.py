import array

kCommonPsshBoxUUID = array.array('B', [0x10, 0x77, 0xEF, 0xEC, 0xC0, 0xB2, 0x4D, 0x02, 0xAC, 0xE3, 0x3C, 0x1E, 0x52, 0xE2, 0xFB, 0x4B])
kClearKeyUUID = array.array('B', [0xE2, 0x71, 0x9D, 0x58, 0xA9, 0x85, 0xB3, 0xC9, 0x78, 0x1A, 0xB0, 0x30, 0xAF, 0x78, 0xD3, 0x0E])

def isClearKeyUUID(uuid):
    return uuid == kCommonPsshBoxUUID or uuid == kClearKeyUUID

def getSupportedCryptoSchemes():
    return [kCommonPsshBoxUUID, kClearKeyUUID]